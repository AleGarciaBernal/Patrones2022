package patrones.prototype.ejemplos.entradas;

public class Equipo {

    private String nombre;
    private int ranking;

    public Equipo(){}

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getRanking() {
        return ranking;
    }

    public void setRanking(int ranking) {
        this.ranking = ranking;
    }
}
